// RegionInfo
export class RegionInfo {
    // fields
    public x: number = 0.0;
    public y: number = 0.0;
    public w: number = 0.0;
    public h: number = 0.0;
    public ID: string = "";
    public color: string = "red";
    // constructor
    constructor() {
        this.x = 0;
        this.y = 0;
        this.w = 0;
        this.h = 0;
        this.ID = "";
        this.color = "red";
    }
}